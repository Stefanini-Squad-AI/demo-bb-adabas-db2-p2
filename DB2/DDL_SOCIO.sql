-- DB2 DDL: migracao ADABAS-SOCIOS-P2 (DBATDP-17)
-- Entidade principal SOCIO e tabela filha 1:N para PERIODICO-PAGAMENTO

CREATE TABLE SOCIO (
    NUMB_SOCIO_PRINCIPAL   DECIMAL(9, 0)  NOT NULL,
    NOME_SOCIO_PRINCIPAL   VARCHAR(40)    NOT NULL,
    DATA_CADASTRO          DATE,
    CATG_SOCIO             SMALLINT,
    INDI_DIVIDA            CHAR(1),
    DATA_BAIXA             DATE,
    HORA_BAIXA             CHAR(5),
    OBSV_SOCIO             VARCHAR(500),
    CONSTRAINT PK_SOCIO PRIMARY KEY (NUMB_SOCIO_PRINCIPAL)
);

-- SUPER1 (Adabas BA+BB): CATG-SOCIO + INDI-DIVIDA - indice composto equivalente
CREATE INDEX IX_SOCIO_SUPER1 ON SOCIO (CATG_SOCIO, INDI_DIVIDA);

CREATE TABLE SOCIO_PERIODICO_PAGAMENTO (
    ID_PERIODICO           BIGINT         NOT NULL GENERATED ALWAYS AS IDENTITY,
    NUMB_SOCIO_PRINCIPAL   DECIMAL(9, 0)  NOT NULL,
    SEQ_PERIODICO          INTEGER        NOT NULL,
    DATA_VENCIMENTO        DATE,
    VALR_MENSALIDADE       DECIMAL(6, 2),
    PAGAMENTO_OK           CHAR(1),
    CONSTRAINT PK_SOCIO_PERIODICO PRIMARY KEY (ID_PERIODICO),
    CONSTRAINT FK_SOCIO_PERIODICO_SOCIO FOREIGN KEY (NUMB_SOCIO_PRINCIPAL)
        REFERENCES SOCIO (NUMB_SOCIO_PRINCIPAL),
    CONSTRAINT UX_SOCIO_PERIODICO_SEQ UNIQUE (NUMB_SOCIO_PRINCIPAL, SEQ_PERIODICO)
);

CREATE INDEX IX_SOCIO_PERIODICO_SOCIO ON SOCIO_PERIODICO_PAGAMENTO (NUMB_SOCIO_PRINCIPAL);
