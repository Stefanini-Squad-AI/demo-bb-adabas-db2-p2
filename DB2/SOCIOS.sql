-- DB2 Schema for SOCIOS (Partners/Members) Migration
-- Parent table: SOCIOS
-- Child table: SOCIOS_PAGAMENTO

CREATE TABLE SOCIOS (
    NUMB_SOCIO_PRINCIPAL DECIMAL(9,0) NOT NULL PRIMARY KEY,
    NOME_SOCIO_PRINCIPAL CHAR(40) NOT NULL,
    DATA_CADASTRO DATE NOT NULL,
    CATG_SOCIO SMALLINT,
    DATA_BAIXA DATE,
    HORA_BAIXA CHAR(12),
    OBSV_SOCIO VARCHAR(500)
);

CREATE TABLE SOCIOS_PAGAMENTO (
    SOCIOS_PAGAMENTO_ID INTEGER NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    SOCIO_ID DECIMAL(9,0) NOT NULL,
    DATA_VENCIMENTO DATE,
    VALR_MENSALIDADE DECIMAL(6,2),
    PAGAMENTO_OK SMALLINT,
    FOREIGN KEY (SOCIO_ID) REFERENCES SOCIOS(NUMB_SOCIO_PRINCIPAL)
);

CREATE INDEX IDX_SOCIOS_PAGAMENTO_SOCIO_ID ON SOCIOS_PAGAMENTO(SOCIO_ID);
